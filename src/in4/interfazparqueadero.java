    private void añadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_añadirActionPerformed
        horaGuardada = LocalDateTime.now();
        i++;
        int numero=i;
        pila.push(texto2.getText());
        pila2.push(texto1.getText());
        pila3.push(LocalDateTime.now());
        horaActual = LocalDateTime.now();
        minutosTranscurridos = ChronoUnit.MINUTES.between(horaGuardada, horaActual);
        pila4.push(minutosTranscurridos);
        if("1".equals(texto2.getText()))
        {
            vp=(int) (minutosTranscurridos*60);
        }
        else
            if("2".equals(texto2.getText()))
            {
                vp=(int) (minutosTranscurridos*30);
            }
          else
            if("3".equals(texto2.getText()))
            {
                vp=(int) (minutosTranscurridos*20);
            }
        pila5.push(vp);
        dmt.addRow(new Object[]{numero,pila2.peek(),pila.peek(),pila3.peek(),pila4.peek(),pila5.peek()});
        texto1.setText(" ");
        texto2.setText(" ");
    }//GEN-LAST:event_añadirActionPerformed

