    private void actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarActionPerformed
        TableModel model = tabla.getModel();
        int tarifa=0;
        min= (LocalDateTime) model.getValueAt(tabla.getSelectedRow(),3);
        horaActual = LocalDateTime.now();
        minutosTranscurridos = ChronoUnit.MINUTES.between(min, horaActual);
        long vpg=0;
        int tp=(int) Float.parseFloat((String) model.getValueAt(tabla.getSelectedRow(),2));
    switch (tp) {
        case 1:
            tarifa=60;
            break;
        case 2:
            tarifa=30;
            break;
        case 3:
            tarifa=20;
            break;
        default:
            break;
    }
        vpg=minutosTranscurridos*tarifa;
        dmt.setValueAt(minutosTranscurridos, tabla.getSelectedRow(), 4);
        dmt.setValueAt(vpg, tabla.getSelectedRow(), 5);
        
    }//GEN-LAST:event_actualizarActionPerformed

   
